import Foundation
import UIKit

extension UIColor {
    
    static func whiteColor() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    static func blackColor() -> UIColor {
        return #colorLiteral(red: 0.08166426842, green: 0.08166426842, blue: 0.08166426842, alpha: 1)
    }
    
    static func lightGray() -> UIColor {
        return #colorLiteral(red: 0.5444205403, green: 0.5444338322, blue: 0.5444267392, alpha: 1)
    }
}
